  - rewrite Hdomn, let_result_world_on_dom, Hdom. subst X. reflexivity.
  - intros σν. split.
    + intros Hσν.
      assert (Hσm : (m : World) (store_restrict σν X)).
      {
        rewrite <- Hrestr.
        exists σν. split; [exact Hσν | reflexivity].
      }
