        as [τ' [Hτ'n [Hτ'X Hτ'ν]]].
      assert (Hτ'_eq : τ' = <[ν := vx]> σ).
      {
        assert (Hid : store_restrict τ' (X ∪ {[ν]}) = τ').
        {
          apply store_restrict_idemp.
          pose proof (wfworld_store_dom n τ' Hτ'n) as Hdomτ'.
