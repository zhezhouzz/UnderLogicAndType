        rewrite Henv_eq.
        subst X.
        replace (store_restrict σ (fv_tm e))
          with (store_restrict (store_restrict σ (dom Σ)) (fv_tm e)).
        - apply closed_env_restrict.
          exact (proj1 (Hclosed σ Hσm)).
        - store_norm. replace (dom Σ ∩ fv_tm e) with (fv_tm e) by set_solver.
          reflexivity.
